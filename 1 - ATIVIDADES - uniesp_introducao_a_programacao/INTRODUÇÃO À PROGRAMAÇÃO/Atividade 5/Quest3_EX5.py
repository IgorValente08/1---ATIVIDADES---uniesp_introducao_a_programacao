"""Elabore um algoritmo que leia o valor de dois números inteiros e a operacão aritimética desejada;
calcule, então, a resposta adequada. Utilize os símbolos da tabela a seguir para ler qual operacão
aritmética escolhida."""

primeiro = float(input("Digite o primeiro número: "))
segundo = float(input("Digite o segundo número: "))

print("Resultados")

print(primeiro + segundo)

print(primeiro * segundo)

print(primeiro / segundo)

print(primeiro ** segundo)

